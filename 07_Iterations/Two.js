// while loop

let index = 0;

while (index <= 10) {
  console.log(`The value of index is ${index}`);
  index = index + 2;
}

let myArray = ["flash", "batman", "superman"];

let arr = 0;
while (arr < myArray.length) {
  console.log(`The value is ${myArray[arr]}`);
  arr = arr + 1;
}


// do while loop

let score =0;

do{
  console.log(`The value is ${score}`);
  score++;
  
}while(score <= 10)

let marks =11;

do{
  console.log(`The value is ${marks}`);
  marks++;
  
}while(marks <= 10)

